do
sleep "${ASYSTEM_HEALTHCHECK_INTERVAL_SECONDS:-1}";
done' _ "${POSITIONAL_ARGS[@]}" || ( echo "Health check timed out after ${ASYSTEM_HEALTHCHECK_TIMEOUT_SECONDS:-30}s" >&2; false )
