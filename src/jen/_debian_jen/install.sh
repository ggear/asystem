#!/bin/bash

################################################################################
# Wireless
################################################################################
[ ! -f /etc/modprobe.d/blacklist-brcmfmac.conf ] && echo "blacklist brcmfmac" | tee -a /etc/modprobe.d/blacklist-brcmfmac.conf

################################################################################
# Samba
################################################################################
service smbd stop
systemctl disable smbd
service nmbd stop
systemctl disable nmbd

################################################################################
# Regenerate initramfs
################################################################################
update-initramfs -u
